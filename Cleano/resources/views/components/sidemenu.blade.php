// Function to add 'dash_active' class to the specified button
function addActiveClass(buttonId) {
    var clickedButton = document.getElementById(buttonId);
    // Remove 'dash_active' class from all buttons
    var buttons = document.querySelectorAll('button');
    buttons.forEach(function(button) {
        button.classList.remove('dash_active');
    });
    // Add 'dash_active' class to the clicked button
    clickedButton.classList.add('dash_active');
    // Store the id of the clicked button in sessionStorage
    sessionStorage.setItem('activeButton', buttonId);
}

// Check if there's a previously clicked button and add the class accordingly
window.onload = function() {
    var activeButtonId = sessionStorage.getItem('activeButton');
    if (activeButtonId) {
        var activeButton = document.getElementById(activeButtonId);
        activeButton.classList.add('dash_active');
    }
};
</script>